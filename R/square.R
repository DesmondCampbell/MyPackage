#' Square a number
#'
#' Takes a numeric and squares it
#' @param x numeric to be squared
#' @return squared value
#' @export
square <- function(x) x^2


#' Cube  a number
#'
#' Takes a numeric and cubes it
#' @param x numeric to be cubed
#' @return cubed value
#' @export
cube <- function(x) x^3
